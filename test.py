a = 1
b = 2

# 1. 先定义一个真正的装饰器（给函数穿外套）
def 计时外套(原本的函数):
    def 穿外套后的新函数(*args, **kwargs):
        print("⏰ 准备开始计算...")
        result = 原本的函数(*args, **kwargs)  # 执行原本的求和逻辑，并接住它的返回值
        print("✅ 计算结束！")
        return result  # 把原本函数的计算结果交还出去
    return 穿外套后的新函数

# 2. 用 @ 语法糖把外套披在业务函数上
@计时外套
def sum():
    # 正常开发中，计算和打印要分开。这里只负责计算并返回结果
    return a + b  

# 3. 调用加了装饰器的函数，并用一个变量接住它的返回值
最终结果 = sum()
print(f"最终拿到的计算结果是: {最终结果}")